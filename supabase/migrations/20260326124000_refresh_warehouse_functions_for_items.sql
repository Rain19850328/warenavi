create or replace function public.warehouse_set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = timezone('utc', now());
  return new;
end;
$$;

create table if not exists public.items (
  code text primary key,
  name text not null default '',
  volume_width numeric(12, 2) not null default 0,
  volume_length numeric(12, 2) not null default 0,
  volume_height numeric(12, 2) not null default 0,
  location_code text not null default '',
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.warehouse_racks (
  rack_code text not null,
  item_code text not null references public.items(code) on delete cascade,
  quantity integer not null default 0 check (quantity >= 0),
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  primary key (rack_code, item_code)
);

create table if not exists public.daily_stock (
  sku_cd text not null references public.items(code) on delete cascade,
  stock_date date not null,
  stock_cnt_real integer not null default 0,
  created_at timestamptz not null default timezone('utc', now()),
  primary key (sku_cd, stock_date)
);

create table if not exists public.warehouse_movements (
  id bigint generated by default as identity primary key,
  movement_type text not null check (movement_type in ('inbound', 'outbound', 'move', 'set_location')),
  item_code text not null references public.items(code) on delete cascade,
  rack_code text,
  from_rack text,
  to_rack text,
  quantity integer not null default 0,
  note text not null default '',
  created_at timestamptz not null default timezone('utc', now())
);

create index if not exists items_location_code_idx
  on public.items (location_code);

create index if not exists warehouse_racks_item_code_idx
  on public.warehouse_racks (item_code);

create index if not exists daily_stock_sku_latest_idx
  on public.daily_stock (sku_cd, stock_date desc);

