create or replace function public.warehouse_row_bays(p_row_label text)
returns integer
language sql
immutable
as $$
  select case upper(trim(coalesce(p_row_label, '')))
    when 'SR1' then 21
    when 'SR2' then 11
    when 'SR3' then 21
    else 0
  end
$$;

create or replace function public.warehouse_row_levels(p_row_label text)
returns integer
language sql
immutable
as $$
  select case upper(trim(coalesce(p_row_label, '')))
    when 'SR1' then 3
    when 'SR2' then 6
    when 'SR3' then 3
    else 0
  end
$$;

create or replace function public.warehouse_level_height(p_level integer)
returns integer
language sql
immutable
as $$
  select case coalesce(p_level, 0)
    when 1 then 120
    when 2 then 100
    when 3 then 180
    when 4 then 100
    when 5 then 100
    when 6 then 100
    else 100
  end
$$;

create or replace function public.warehouse_level_capacity(p_level integer)
returns integer
language sql
immutable
as $$
  select 110 * 110 * public.warehouse_level_height(p_level)
$$;

create or replace function public.warehouse_sr2_level_to_letter(p_level integer)
returns text
language plpgsql
immutable
as $$
begin
  if p_level is null or p_level < 1 or p_level > 6 then
    raise exception 'Invalid SR2 level: %', p_level;
  end if;
  return chr(65 + (6 - p_level));
end;
$$;

create or replace function public.warehouse_sr2_letter_to_level(p_letter text)
returns integer
language plpgsql
immutable
as $$
declare
  v_letter text := upper(left(trim(coalesce(p_letter, '')), 1));
begin
  if v_letter < 'A' or v_letter > 'F' then
    raise exception 'Invalid SR2 level letter: %', p_letter;
  end if;
  return 6 - (ascii(v_letter) - ascii('A'));
end;
$$;

create or replace function public.warehouse_cell_code(p_row_label text, p_bay integer, p_level integer)
returns text
language plpgsql
immutable
as $$
declare
  v_row text := upper(trim(coalesce(p_row_label, '')));
begin
  if v_row = 'SR2' then
    return format('SR2-%s-%s', public.warehouse_sr2_level_to_letter(p_level), lpad(p_bay::text, 2, '0'));
  end if;

  return format('%s-%s-%s', v_row, lpad(p_bay::text, 2, '0'), lpad(p_level::text, 2, '0'));
end;
$$;

create or replace function public.warehouse_parse_rack_code(p_code text)
returns table(row_label text, bay integer, level integer)
language plpgsql
immutable
as $$
declare
  s text := upper(trim(coalesce(p_code, '')));
  m text[];
begin
  m := regexp_match(s, '^SR2-([A-F])-([0-9]{2})$');
  if m is not null then
    row_label := 'SR2';
    bay := m[2]::integer;
    level := public.warehouse_sr2_letter_to_level(m[1]);
    return next;
    return;
  end if;

  m := regexp_match(s, '^SR2-([0-9]{2})-([0-9]{2})$');
  if m is not null then
    row_label := 'SR2';
    bay := m[1]::integer;
    level := m[2]::integer;
    return next;
    return;
  end if;

  m := regexp_match(s, '^SR([0-9]{1,2})-([0-9]{2})-([0-9]{2})$');
  if m is not null then
    row_label := format('SR%s', m[1]::integer);
    bay := m[2]::integer;
    level := m[3]::integer;
    return next;
    return;
  end if;

  m := regexp_match(s, '^SR-([0-9]{2})-([A-F])-([0-9]{2})$');
  if m is not null and m[1] = '02' then
    row_label := 'SR2';
    bay := m[3]::integer;
    level := public.warehouse_sr2_letter_to_level(m[2]);
    return next;
    return;
  end if;

  m := regexp_match(s, '^SR-([0-9]{2})-([0-9]{2})-([0-9]{2})$');
  if m is not null then
    row_label := format('SR%s', m[1]::integer);
    bay := m[2]::integer;
    level := m[3]::integer;
    return next;
    return;
  end if;
end;
$$;

create or replace function public.warehouse_to_canonical_code(p_code text)
returns text
language plpgsql
immutable
as $$
declare
  v_row text;
  v_bay integer;
  v_level integer;
begin
  select row_label, bay, level
    into v_row, v_bay, v_level
  from public.warehouse_parse_rack_code(p_code)
  limit 1;

  if v_row is null then
    return trim(coalesce(p_code, ''));
  end if;

  return public.warehouse_cell_code(v_row, v_bay, v_level);
end;
$$;

create or replace function public.warehouse_has_wildcard_00(p_location text)
returns boolean
language plpgsql
immutable
as $$
declare
  v_part text;
  v_parts text[] := string_to_array(public.warehouse_to_canonical_code(p_location), '-');
begin
  if v_parts is null then
    return false;
  end if;

  foreach v_part in array v_parts loop
    if v_part = '00' then
      return true;
    end if;
  end loop;

  return false;
end;
$$;

create or replace function public.warehouse_location_in_use(p_location text, p_ignore_item_code text default null)
returns text
language plpgsql
stable
as $$
declare
  v_location text;
  v_code text;
begin
  if public.warehouse_has_wildcard_00(p_location) then
    return null;
  end if;

  v_location := public.warehouse_to_canonical_code(p_location);

  select code
    into v_code
  from public.items
  where location_code = v_location
    and (p_ignore_item_code is null or code <> p_ignore_item_code)
  limit 1;

  return v_code;
end;
$$;

