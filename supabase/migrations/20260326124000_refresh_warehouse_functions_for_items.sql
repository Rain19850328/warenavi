create or replace function public.warehouse_get_items(p_q text default '', p_limit integer default 500)
returns jsonb
language plpgsql
stable
as $$
declare
  v_q text := trim(coalesce(p_q, ''));
  v_limit integer := least(greatest(coalesce(p_limit, 500), 1), 5000);
begin
  return jsonb_build_object(
    'items',
    coalesce((
      select jsonb_agg(
        jsonb_build_object(
          'code', t.code,
          'name', t.name,
          'w', t.volume_width,
          'l', t.volume_length,
          'h', t.volume_height,
          'unit_cm3', (
            greatest(t.volume_width, 0::numeric) *
            greatest(t.volume_length, 0::numeric) *
            greatest(t.volume_height, 0::numeric)
          )::integer,
          'location', coalesce(t.location_code, '')
        )
        order by t.code, t.name
      )
      from (
        select *
        from public.items i
        where v_q = ''
          or i.code ilike '%' || v_q || '%'
          or i.name ilike '%' || v_q || '%'
          or i.location_code ilike '%' || v_q || '%'
        order by i.code asc, i.name asc
        limit v_limit
      ) t
    ), '[]'::jsonb)
  );
end;
$$;

create or replace function public.warehouse_get_items_with_stock(p_q text default '', p_limit integer default 300)
returns jsonb
language plpgsql
stable
as $$
declare
  v_q text := trim(coalesce(p_q, ''));
  v_limit integer := least(greatest(coalesce(p_limit, 300), 1), 5000);
begin
  return jsonb_build_object(
    'items',
    coalesce((
      with latest_stock as (
        select distinct on (ds.sku_cd)
          ds.sku_cd,
          ds.stock_cnt_real
        from public.daily_stock ds
        order by ds.sku_cd, ds.stock_date desc
      )
      select jsonb_agg(
        jsonb_build_object(
          'code', t.code,
          'name', t.name,
          'w', t.volume_width,
          'l', t.volume_length,
          'h', t.volume_height,
          'unit_cm3', (
            greatest(t.volume_width, 0::numeric) *
            greatest(t.volume_length, 0::numeric) *
            greatest(t.volume_height, 0::numeric)
          )::integer,
          'location', coalesce(t.location_code, ''),
          'stock_qty', coalesce(ls.stock_cnt_real, 0)
        )
        order by t.code, t.name
      )
      from (
        select *
        from public.items i
        where v_q = ''
          or i.code ilike '%' || v_q || '%'
          or i.name ilike '%' || v_q || '%'
          or i.location_code ilike '%' || v_q || '%'
        order by i.code asc, i.name asc
        limit v_limit
      ) t
      left join latest_stock ls on ls.sku_cd = t.code
    ), '[]'::jsonb)
  );
end;
$$;

create or replace function public.warehouse_get_cells(p_row text default 'SR1')
returns jsonb
language plpgsql
stable
as $$
declare
  v_row text := upper(trim(coalesce(p_row, 'SR1')));
  v_bays integer := public.warehouse_row_bays(v_row);
  v_levels integer := public.warehouse_row_levels(v_row);
  v_result jsonb;
begin
  if v_bays = 0 or v_levels = 0 then
    raise exception 'Unknown row: %', p_row;
  end if;

  with grid as (
    select
      v_row as row_label,
      b as bay,
      l as level,
      public.warehouse_cell_code(v_row, b, l) as code,
      public.warehouse_level_capacity(l) as capacity_cm3
    from generate_series(1, v_bays) as b
    cross join generate_series(1, v_levels) as l
  ),
  cell_items as (
    select
      public.warehouse_to_canonical_code(sr.rack_code) as rack_code,
      i.code as sku,
      coalesce(i.name, '') as name,
      sr.quantity as qty,
      (
        greatest(coalesce(i.volume_width, 0), 0::numeric) *
        greatest(coalesce(i.volume_length, 0), 0::numeric) *
        greatest(coalesce(i.volume_height, 0), 0::numeric) *
        greatest(sr.quantity, 0)
      )::bigint as used_cm3,
      coalesce(i.location_code, '') as location
    from public.warehouse_racks sr
    join public.items i on i.code = sr.item_code
    join lateral public.warehouse_parse_rack_code(sr.rack_code) pr on true
    where pr.row_label = v_row
  ),
  cell_json as (
    select
      g.bay,
      g.level,
      jsonb_build_object(
        'code', g.code,
        'bay', g.bay,
        'level', g.level,
        'capacity_cm3', g.capacity_cm3,
        'total_cm3', coalesce(sum(ci.used_cm3), 0)::integer,
        'kinds', count(ci.sku)::integer,
        'items', coalesce(
          jsonb_agg(
            jsonb_build_object(
              'sku', ci.sku,
              'name', ci.name,
              'qty', ci.qty,
              'used_cm3', ci.used_cm3,
              'location', ci.location
            )
            order by ci.used_cm3 desc, ci.sku asc
          ) filter (where ci.sku is not null),
          '[]'::jsonb
        )
      ) as payload
    from grid g
    left join cell_items ci on ci.rack_code = g.code
    group by g.code, g.bay, g.level, g.capacity_cm3
    order by g.bay, g.level
  )
  select jsonb_build_object(
    'row', v_row,
    'bays', v_bays,
    'levels', v_levels,
    'cells', coalesce(jsonb_agg(payload order by bay, level), '[]'::jsonb)
  )
    into v_result
  from cell_json;

  return v_result;
end;
$$;

create or replace function public.warehouse_search_racks(p_q text default '', p_limit integer default 500)
returns jsonb
language plpgsql
stable
as $$
declare
  v_q text := trim(coalesce(p_q, ''));
  v_limit integer := least(greatest(coalesce(p_limit, 500), 1), 5000);
  v_canon text := public.warehouse_to_canonical_code(v_q);
begin
  return jsonb_build_object(
    'q', v_q,
    'results',
    coalesce((
      with rows_ as (
        select
          public.warehouse_to_canonical_code(sr.rack_code) as rack_code,
          pr.row_label,
          pr.bay,
          pr.level,
          i.code as sku,
          coalesce(i.name, '') as name,
          sr.quantity as qty,
          coalesce(i.location_code, '') as location,
          (
            greatest(coalesce(i.volume_width, 0), 0::numeric) *
            greatest(coalesce(i.volume_length, 0), 0::numeric) *
            greatest(coalesce(i.volume_height, 0), 0::numeric) *
            greatest(sr.quantity, 0)
          )::integer as used_cm3,
          public.warehouse_level_capacity(pr.level) as capacity_cm3
        from public.warehouse_racks sr
        join public.items i on i.code = sr.item_code
        join lateral public.warehouse_parse_rack_code(sr.rack_code) pr on true
        where v_q = ''
          or i.code ilike '%' || v_q || '%'
          or i.name ilike '%' || v_q || '%'
          or i.location_code ilike '%' || v_q || '%'
          or public.warehouse_to_canonical_code(sr.rack_code) ilike '%' || v_q || '%'
          or public.warehouse_to_canonical_code(sr.rack_code) ilike '%' || v_canon || '%'
        order by public.warehouse_to_canonical_code(sr.rack_code) asc, i.code asc
        limit v_limit
      )
      select jsonb_agg(
        jsonb_build_object(
          'rack_code', t.rack_code,
          'row', t.row_label,
          'bay', t.bay,
          'level', t.level,
          'sku', t.sku,
          'name', t.name,
          'qty', t.qty,
          'capacity_cm3', t.capacity_cm3,
          'used_cm3', t.used_cm3,
          'rate', case
            when t.capacity_cm3 <= 0 then 0
            else floor((t.used_cm3::numeric / t.capacity_cm3::numeric) * 100)::integer
          end,
          'location', t.location
        )
      )
      from rows_ t
    ), '[]'::jsonb)
  );
end;
$$;

create or replace function public.warehouse_post_inbound(p_rack_code text, p_item_code text, p_qty integer)
returns jsonb
language plpgsql
as $$
declare
  v_rack text := public.warehouse_to_canonical_code(p_rack_code);
  v_row text;
  v_bay integer;
  v_level integer;
begin
  if coalesce(p_qty, 0) <= 0 then
    raise exception 'qty must be greater than 0';
  end if;

  if not exists (select 1 from public.items where code = p_item_code) then
    raise exception 'Unknown item_code: %', p_item_code;
  end if;

  select row_label, bay, level
    into v_row, v_bay, v_level
  from public.warehouse_parse_rack_code(v_rack)
  limit 1;

  if v_row is null then
    raise exception 'Invalid rack code: %', p_rack_code;
  end if;

  insert into public.warehouse_racks (rack_code, item_code, quantity)
  values (v_rack, p_item_code, p_qty)
  on conflict (rack_code, item_code)
  do update set quantity = public.warehouse_racks.quantity + excluded.quantity;

  insert into public.warehouse_movements (movement_type, item_code, rack_code, quantity, note)
  values ('inbound', p_item_code, v_rack, p_qty, '');

  return jsonb_build_object(
    'ok', true,
    'row', v_row,
    'cells', public.warehouse_get_cells(v_row)
  );
end;
$$;

create or replace function public.warehouse_post_outbound(p_rack_code text, p_item_code text, p_qty integer)
returns jsonb
language plpgsql
as $$
declare
  v_rack text := public.warehouse_to_canonical_code(p_rack_code);
  v_row text;
  v_bay integer;
  v_level integer;
  v_current integer;
begin
  if coalesce(p_qty, 0) <= 0 then
    raise exception 'qty must be greater than 0';
  end if;

  select row_label, bay, level
    into v_row, v_bay, v_level
  from public.warehouse_parse_rack_code(v_rack)
  limit 1;

  if v_row is null then
    raise exception 'Invalid rack code: %', p_rack_code;
  end if;

  select quantity
    into v_current
  from public.warehouse_racks
  where rack_code = v_rack
    and item_code = p_item_code
  for update;

  if coalesce(v_current, 0) < p_qty then
    raise exception 'Not enough quantity in rack';
  end if;

  update public.warehouse_racks
  set quantity = quantity - p_qty
  where rack_code = v_rack
    and item_code = p_item_code;

  delete from public.warehouse_racks
  where rack_code = v_rack
    and item_code = p_item_code
    and quantity <= 0;

  insert into public.warehouse_movements (movement_type, item_code, rack_code, quantity, note)
  values ('outbound', p_item_code, v_rack, p_qty, '');

  return jsonb_build_object(
    'ok', true,
    'row', v_row,
    'cells', public.warehouse_get_cells(v_row)
  );
end;
$$;

create or replace function public.warehouse_post_move(p_from_rack text, p_to_rack text, p_item_code text, p_qty integer)
returns jsonb
language plpgsql
as $$
declare
  v_from text := public.warehouse_to_canonical_code(p_from_rack);
  v_to text := public.warehouse_to_canonical_code(p_to_rack);
  v_from_row text;
  v_from_bay integer;
  v_from_level integer;
  v_to_row text;
  v_to_bay integer;
  v_to_level integer;
  v_current integer;
  v_rows jsonb;
begin
  if coalesce(p_qty, 0) <= 0 then
    raise exception 'qty must be greater than 0';
  end if;

  if v_from = v_to then
    raise exception 'from_rack and to_rack must differ';
  end if;

  select row_label, bay, level
    into v_from_row, v_from_bay, v_from_level
  from public.warehouse_parse_rack_code(v_from)
  limit 1;

  select row_label, bay, level
    into v_to_row, v_to_bay, v_to_level
  from public.warehouse_parse_rack_code(v_to)
  limit 1;

  if v_from_row is null then
    raise exception 'Invalid from_rack: %', p_from_rack;
  end if;

  if v_to_row is null then
    raise exception 'Invalid to_rack: %', p_to_rack;
  end if;

  select quantity
    into v_current
  from public.warehouse_racks
  where rack_code = v_from
    and item_code = p_item_code
  for update;

  if coalesce(v_current, 0) < p_qty then
    raise exception 'Not enough quantity in source rack';
  end if;

  update public.warehouse_racks
  set quantity = quantity - p_qty
  where rack_code = v_from
    and item_code = p_item_code;

  delete from public.warehouse_racks
  where rack_code = v_from
    and item_code = p_item_code
    and quantity <= 0;

  insert into public.warehouse_racks (rack_code, item_code, quantity)
  values (v_to, p_item_code, p_qty)
  on conflict (rack_code, item_code)
  do update set quantity = public.warehouse_racks.quantity + excluded.quantity;

  insert into public.warehouse_movements (movement_type, item_code, from_rack, to_rack, quantity, note)
  values ('move', p_item_code, v_from, v_to, p_qty, '');

  if v_from_row = v_to_row then
    v_rows := jsonb_build_object(
      v_from_row, public.warehouse_get_cells(v_from_row)
    );
  else
    v_rows := jsonb_build_object(
      v_from_row, public.warehouse_get_cells(v_from_row),
      v_to_row, public.warehouse_get_cells(v_to_row)
    );
  end if;

  return jsonb_build_object(
    'ok', true,
    'rows', v_rows
  );
end;
$$;

create or replace function public.warehouse_post_set_location(p_item_code text, p_location text)
returns jsonb
language plpgsql
as $$
declare
  v_location text;
  v_used_by text;
begin
  if trim(coalesce(p_item_code, '')) = '' then
    raise exception 'item_code is required';
  end if;

  if not exists (select 1 from public.items where code = p_item_code) then
    raise exception 'Unknown item_code: %', p_item_code;
  end if;

  if trim(coalesce(p_location, '')) = '00' then
    update public.items
    set location_code = '00'
    where code = p_item_code;

    insert into public.warehouse_movements (movement_type, item_code, quantity, note)
    values ('set_location', p_item_code, 0, '00');

    return jsonb_build_object('ok', true);
  end if;

  v_location := public.warehouse_to_canonical_code(p_location);
  v_used_by := public.warehouse_location_in_use(v_location, p_item_code);

  if v_used_by is not null then
