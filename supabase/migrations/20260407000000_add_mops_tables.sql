-- 인덱스

create index if not exists idx_mops_orders_date on public.orders(order_date, code);
create index if not exists idx_mops_orders_status on public.orders(shipping_status);
create index if not exists idx_mops_imports_date on public.imports(import_date, code);
create index if not exists idx_mops_finance1_date on public.finance_1st(date, code);
create index if not exists idx_mops_finance1_order on public.finance_1st(order_code);
create index if not exists idx_mops_finance2_date on public.finance_2nd(date, code);
create index if not exists idx_mops_finance2_order on public.finance_2nd(order_code);
create index if not exists idx_mops_matching_text on public.matching_lists(match_text);
create index if not exists idx_mops_listing_ids_item on public.listing_ids(item_code, marketplace_key);
create index if not exists idx_mops_listing_ids_lid on public.listing_ids(listing_id);
create index if not exists idx_mops_item_stocks_updated on public.item_stocks(updated_at);
create index if not exists idx_mops_detailed_result_ymc on public.detailed_result(year_month_channel);
create index if not exists idx_mops_customer_cs_date on public.customer_management(cs_date);

